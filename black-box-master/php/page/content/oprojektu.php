<p class="l">About the project</p>
<p>The Black Box curatorial research project was launched during the pandemic of the new coronavirus in the spring of 2020, with the aim to point out the problems and challenges faced by Czech culture at that time:
<ul>
<li>We wanted to point out that contemporary art and artists found themselves in an existential and existential crisis during quarantine due to the paralysis of art institutions, but also because the view of human life has suddenly been reduced to basic imperatives – to be healthy, to survive, to adapt to a new state of the reality. Therefore, we called on the young artists to engage in shaping the current situation with their own means of expression, even in these extraordinary circumstances. This could be their individual experience or different artistic research strategies for the Covid-19 coronavirus pandemic.</li><br>
<li>We wanted to see the coronavirus pandemic from the outside, deprived of the kitschy pathos of collective sentiment, which enveloped everything with a mist of emotion. Therefore, we provided the collected traces and images created during the pandemic to a non-human entity in the form of an artificial neural network. This curatorial software deals with the collected material in its own way to provide us with the (an)aesthetic interpretation of a collective experience to which we are unable to take a critical distance.</li><br>
